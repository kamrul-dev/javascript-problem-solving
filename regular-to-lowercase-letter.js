// Any regular case to lowercase letter using function

function lowercaseLetter(){
    var name = "KamRuL";
    var resultForLowerCase = name.toLowerCase();
    console.log(resultForLowerCase);
}
lowercaseLetter();



// regular case to uppercase
function uppercaseLetter(){
    var name = "KamRuL";
    var resultForLowerCase = name.toUpperCase();
    console.log(resultForLowerCase);
}
uppercaseLetter();